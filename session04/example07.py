# create a dictionary
my_dict = {
  "key1": "value1",
  "key2": "value2",
  "key3": "value3"
}

# access the value associated with a key
print(my_dict["key1"])  # prints "value1"

# add a new key-value pair to the dictionary
my_dict["key4"] = "value4"

# delete a key-value pair from the dictionary
del my_dict["key2"]
